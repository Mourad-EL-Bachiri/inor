package com.inor.demo.requests;

public class ContactParentRequest {
	
	String firstName_P;
	String lastName_P;
	String adresse;
	String telephone;
	String email;
	public String getFirstName_P() {
		return firstName_P;
	}
	public void setFirstName_P(String firstName_P) {
		this.firstName_P = firstName_P;
	}
	public String getLastName_P() {
		return lastName_P;
	}
	public void setLastName_P(String lastName_P) {
		this.lastName_P = lastName_P;
	}
	public String getAdresse() {
		return adresse;
	}
	public void setAdresse(String adresse) {
		this.adresse = adresse;
	}
	public String getTelephone() {
		return telephone;
	}
	public void setTelephone(String telephone) {
		this.telephone = telephone;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	
	

}
